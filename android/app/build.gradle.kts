            }
            isMinifyEnabled = false
            isShrinkResources = false
            proguardFiles(
                getDefaultProguardFile("proguard-android-optimize.txt"),
                "proguard-rules.pro"
            )
        }
        getByName("debug") {
            // Dùng debug.keystore mặc định của Android
            signingConfig = signingConfigs.getByName("debug")
            // Nếu muốn tắt shrink/minify ở debug, giữ nguyên mặc định (false)
        }
